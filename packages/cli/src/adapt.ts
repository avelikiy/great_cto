function getCliVersion(): string {
  try {
    const here = dirname(new URL(import.meta.url).pathname);
    const pkg = JSON.parse(readFileSync(join(here, "..", "package.json"), "utf8"));
    return pkg.version ?? "unknown";
  } catch {
    return "unknown";
  }
}

function writeFile(path: string, content: string, dryRun: boolean): boolean {
  if (dryRun) {
    console.log(`would write: ${path} (${content.length} bytes)`);
    return false;
  }
  mkdirSync(dirname(path), { recursive: true });
  const existed = existsSync(path);
  writeFileSync(path, content);
  console.log(`  ${existed ? "✎ updated" : "✓ wrote"}  ${path}`);
  return true;
}

// ── Claude Code adapter ────────────────────────────────────────────────────

function adaptClaude(cwd: string, meta: ProjectMeta, dryRun: boolean): string[] {
  const out: string[] = [];
  const agentsBody = getAgentsCore(meta);
  const claudeBody = `# CLAUDE.md

> Project guidance for Claude Code. The full agent contract lives in
> \`AGENTS.md\` — please read that first. This file adds Claude Code-specific
> overrides only.

## Cross-tool contract

See [AGENTS.md](./AGENTS.md) for the full project context, gates, and
conventions. Generated by \`great-cto adapt\`.

## Claude Code specifics

- The great_cto plugin orchestrates specialist agents — pipeline runs
  through \`/start\`, \`/inbox\`, \`/save\`, \`/spec\`, etc.
- Memory is layered: \`.great_cto/PROJECT.md\` (L1) → \`lessons.md\` (L3) →
  \`~/.great_cto/decisions.md\` (L4 cross-project ADR log).
- Run \`great-cto board\` (\`http://localhost:3141\`) for the kanban + metrics
  + memory + agents view.

## Quick links

- \`/spec\` — interview → requirements.md + design.md + tasks.md
- \`/start "..."\` — kick off a feature pipeline
- \`/inbox\` — see what needs your decision
- \`/agent-review\` — performance scorecard for agents

`;
  if (writeFile(join(cwd, "AGENTS.md"), agentsBody, dryRun)) out.push("AGENTS.md");
  if (writeFile(join(cwd, "CLAUDE.md"), claudeBody, dryRun)) out.push("CLAUDE.md");

  // Cross-AI config files based on ai_tools in PROJECT.md
  for (const tool of meta.aiTools) {
    switch (tool) {
      case "cursor": {
        const path = join(cwd, ".cursorrules");
        if (writeFile(path, getCursorrules(meta), dryRun)) out.push(".cursorrules");
        break;
      }
      case "copilot": {
        const path = join(cwd, ".github", "copilot-instructions.md");
        if (writeFile(path, getCopilotInstructions(meta), dryRun)) out.push(".github/copilot-instructions.md");
        break;
      }
      case "windsurf": {
        const path = join(cwd, ".windsurfrules");
        if (writeFile(path, getWindsurfrules(meta), dryRun)) out.push(".windsurfrules");
        break;
      }
      case "aider": {
        const path = join(cwd, ".aider.conf.yml");
        if (writeFile(path, getAiderConf(meta), dryRun)) out.push(".aider.conf.yml");
        break;
      }
      case "codex": {
        // Write hooks.json + config.toml fragment to .codex/ (project-scoped, trusted)
        const defaultSkillDir = `${process.env.HOME ?? "~"}/.codex/skills/great_cto`;
        const hooksPath = join(cwd, ".codex", "hooks.json");
        const configPath = join(cwd, ".codex", "great_cto.toml");
        if (writeFile(hooksPath, getCodexHooksJson(defaultSkillDir), dryRun)) out.push(".codex/hooks.json");
        if (writeFile(configPath, getCodexConfigFragment(meta), dryRun)) out.push(".codex/great_cto.toml");
        if (!dryRun) {
          console.log("  ⚠  Codex: merge .codex/great_cto.toml sections into ~/.codex/config.toml");
          console.log("     Then restart Codex to activate hooks and MCP server.");
        }
        break;
      }
      case "claude-code":
        // Already handled above (CLAUDE.md + AGENTS.md)
        break;
    }
  }

  return out;
}

// ── Main entry ─────────────────────────────────────────────────────────────

export async function runAdapt(args: AdaptArgs): Promise<number> {
  const meta = readProjectMeta(args.cwd);
  if (!meta.hasGreatCto) {
    console.error("FAIL: no .great_cto/PROJECT.md found.");
    console.error("Run `npx great-cto init` first to bootstrap the project.");
    return 1;
  }

  const toolsList = meta.aiTools.join(", ");
  console.log(`great-cto adapt → ${toolsList}${args.dryRun ? " (dry-run)" : ""}`);
  console.log(`  archetype: ${meta.archetype}  compliance: ${meta.compliance.join(", ") || "none"}`);
  console.log(`  ai_tools:  ${toolsList}`);
  console.log("");

  const written = adaptClaude(args.cwd, meta, args.dryRun);

  if (!args.dryRun) {
    console.log("");
    console.log(`✓ generated ${written.length} file(s): ${written.join(", ")}`);
    console.log(`  Re-run after editing .great_cto/PROJECT.md or adding tools to ai_tools: [] to refresh.`);
    if (!meta.aiTools.includes("cursor") && !meta.aiTools.includes("copilot") && !meta.aiTools.includes("windsurf")) {
      console.log(`  Tip: add other AI tools to ai_tools: [] in PROJECT.md to generate .cursorrules, copilot-instructions.md, etc.`);
    }
  }
  return 0;
}
