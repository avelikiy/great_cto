// great-cto adapt — AI config generator.
//
// Writes CLAUDE.md + AGENTS.md from .great_cto/PROJECT.md context.
// Also generates cross-AI configs for Cursor, Copilot, Windsurf, Aider
// when ai_tools is set in PROJECT.md.
//
// Usage:
//   great-cto adapt            Generate all configs (reads ai_tools from PROJECT.md)
//   great-cto adapt --dry-run  Show what would be written

import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";

export type Platform = "claude";

/** AI tools that great-cto can generate config files for. */
export type AiTool = "claude-code" | "cursor" | "copilot" | "windsurf" | "aider" | "codex";

export interface AdaptArgs {
  platform: Platform;
  dryRun: boolean;
  cwd: string;
}

interface ProjectMeta {
  archetype: string;
  compliance: string[];
  owners: string;
  hasGreatCto: boolean;
  aiTools: AiTool[];
  projectName: string;
}

const VALID_AI_TOOLS: AiTool[] = ["claude-code", "cursor", "copilot", "windsurf", "aider", "codex"];

function parseAiTools(text: string): AiTool[] {
  const line = text.match(/^ai_tools:\s*(.+)$/m)?.[1] ?? "";
  // Support: [claude-code, cursor] or claude-code, cursor or claude-code
  const raw = line.replace(/[\[\]]/g, "").split(/[,\s]+/).map(s => s.trim()).filter(Boolean);
  return raw.filter((t): t is AiTool => VALID_AI_TOOLS.includes(t as AiTool));
}

function readProjectMeta(cwd: string): ProjectMeta {
  const projectMd = join(cwd, ".great_cto", "PROJECT.md");
  if (!existsSync(projectMd)) {
    return { archetype: "unknown", compliance: [], owners: "", hasGreatCto: false, aiTools: ["claude-code"], projectName: "project" };
  }
  const text = readFileSync(projectMd, "utf8");
  const archetype = (text.match(/^primary:\s*(\S+)/m)?.[1] ?? "unknown").trim();
  const complianceLine = text.match(/^compliance:\s*(.+)$/m)?.[1] ?? "";
  const compliance = complianceLine
    .split(/[,\s]+/)
    .map(s => s.trim())
    .filter(Boolean);
  const owners = (text.match(/^owners?:\s*(.+)$/m)?.[1] ?? "").trim();
  const aiTools = parseAiTools(text);
  // Infer project name from first H1 heading
  const projectName = (text.match(/^#\s+(.+)$/m)?.[1] ?? "project").trim();
  return { archetype, compliance, owners, hasGreatCto: true, aiTools: aiTools.length > 0 ? aiTools : ["claude-code"], projectName };
}

/**
 * Project Constitution — universal hard constraints injected into every AI config.
 * Inspired by Spec Driven Development (FredAntB/Spec-Driven-Development).
 */
function getConstitutionBlock(meta: ProjectMeta): string {
  const hasSpec = existsSync(join(process.cwd(), "requirements.md"));
  const specSection = hasSpec ? `
## Spec files (source of truth)

  requirements.md  — What the system must do
  design.md        — How the system is structured
  tasks.md         — Ordered implementation plan

MANDATORY BEFORE ANY ACTION:
  0. If CONTEXT.md exists, read it first — it has session state
  1. Read requirements.md — understand what is in scope
  2. Read design.md — understand how it is structured
  3. Read tasks.md — find the next incomplete [ ] task
` : `
## Session context

  0. If CONTEXT.md exists, read it first — it has the active task and session state
  1. Read .great_cto/PROJECT.md — archetype, compliance gates, team mode
  2. Read .great_cto/lessons.md if present — past mistakes and patterns
`;

  return `
═══════════════════════════════════════════════════════════
PROJECT CONSTITUTION — ${meta.projectName.toUpperCase()}
Generated by great-cto adapt · ${new Date().toISOString().slice(0, 10)}
═══════════════════════════════════════════════════════════
${specSection}
HARD CONSTRAINTS (no exceptions):
  ✗ Never implement requirements not explicitly defined in requirements.md
  ✗ Never alter the data model without updating design.md first
  ✗ Never mark a task [x] without verifying its acceptance criterion
  ✗ Never create files not listed or implied in design.md
  ✗ Never guess when a requirement is ambiguous — ask instead
  ✗ Never skip gates (security-officer, qa-engineer) — use /waiver with reason

AFTER COMPLETING A TASK:
  1. Run the verification step listed in tasks.md (or acceptance criterion)
  2. Mark the task [x] in tasks.md
  3. Update CONTEXT.md resume block with the next active task
  4. Record any divergences from design.md in CONTEXT.md

IF IMPLEMENTATION MUST DEVIATE FROM DESIGN:
  1. Stop immediately
  2. Describe the conflict clearly
  3. Wait for explicit approval
  4. Update design.md FIRST, then implement
═══════════════════════════════════════════════════════════
`;
}

/**
 * AGENTS.md — Claude Code reads this as the cross-tool agent contract.
 */
function getAgentsCore(meta: ProjectMeta): string {
  const compliance = meta.compliance.length > 0
    ? meta.compliance.map(c => `- ${c}`).join("\n")
    : "_(none auto-detected — set in .great_cto/PROJECT.md)_";

  const constitution = getConstitutionBlock(meta);

  return `# AGENTS.md
${constitution}

> Project agent contract for Claude Code. Generated by \`great-cto adapt\` —
> re-run after editing \`.great_cto/PROJECT.md\`.

## Project context

- **Archetype:** ${meta.archetype}
- **Compliance gates:**
${compliance.split("\n").map(l => "  " + l).join("\n")}
- **Owners:** ${meta.owners || "_(unset)_"}

## How agents should work in this repo

1. **Before touching code** — read \`.great_cto/PROJECT.md\` (archetype +
   constraints) and \`.great_cto/lessons.md\` if present (past mistakes).
2. **Before proposing a fix** — query \`~/.great_cto/decisions.md\` for ADRs
   on related topics. Solved problems should stay solved.
3. **Before merging** — run \`npx great-cto ci\` locally. Same gate as CI.
4. **On failure / incident** — append to \`.great_cto/lessons.md\`. After 3
   occurrences across projects, \`/crystallize\` promotes to global pattern.

## Required gates (do not bypass)

| Gate | When | Owner |
|---|---|---|
| security-officer | Any change touching auth, payments, PII | @security |
| qa-engineer | Any feature merge | @qa |
| performance-engineer | Any change to hot path | @perf |
| db-migration-reviewer | Any \`migrations/\` change | @data |

Override with \`/waiver\` and a written reason. Auto-expires in 14 days.

## Available tools (via MCP)

When this repo is opened in Claude Code, \`great-cto mcp\` exposes:

- \`scan\` — OWASP LLM Top 10 + 24 rules · returns findings
- \`list_rules\` — full rule catalogue
- \`detect_archetype\` — archetype + compliance for any path
- \`estimate_cost\` — LLM vs human time estimate for a task
- \`query_decisions\` — search ADR log

Configure Claude Code to launch:
\`\`\`bash
npx great-cto mcp
\`\`\`

## Subagent routing (do not default to general-purpose)

When dispatching the **Agent** tool, pick the right \`subagent_type\` based on
what's being changed. \`general-purpose\` is a fallback, **not** the default —
using it for pattern-matched work silently skips specialist review.

| Trigger (file pattern OR topic in prompt) | Use \`subagent_type:\` |
|---|---|
| \`migrations/\`, \`schema.sql\`, ORM migration files | \`db-migration-reviewer\` |
| \`auth/\`, OAuth/SAML/JWT, login flow, password reset | \`security-officer\` |
| Payment endpoints, \`stripe.\`, webhooks, refund flow | \`pci-reviewer\` |
| Prompts in \`prompts/\`, RAG, tool definitions, LLM-facing strings | \`ai-security-reviewer\` |
| Eval suites, golden citation tests, prompt regression | \`ai-eval-engineer\` |
| Play Store, App Store, iOS/Android release | \`mobile-store-reviewer\` |
| API contract: OpenAPI, GraphQL schema, webhook signatures | \`api-platform-reviewer\` |
| Voice/IVR/telephony, Twilio, recording-consent, TCPA | \`voice-ai-reviewer\` |
| Clinical / SaMD / FDA / FHIR / PHI | \`ai-clinical-reviewer\`, \`fda-reviewer\` |
| Wearable telemetry, HealthKit, Health Connect, Garmin, Samsung Health, fitness AI, mental health AI, nutrition AI, supplement AI, physician HITL | \`digital-health-reviewer\` |
| HR-AI, hiring, AEDT, resume screening | \`hr-ai-reviewer\` |
| Infra-as-code: Terraform / Helm / CDK / Pulumi | \`infra-reviewer\` |
| Performance regression, hot path, p99 budgets | \`performance-engineer\` |
| Browser extension manifest, MV3 permissions | \`web-store-reviewer\` |
| New feature implementation (TDD) | \`senior-dev\` |
| Architecture decisions, ADRs, scaling questions | \`architect\` |
| Planning a feature into tasks (Beads), dependency graph | \`pm\` |
| QA report after impl, coverage + acceptance | \`qa-engineer\` |
| Deploy / canary / rollback / SLO | \`devops\` |
| Production incident triage, P0 postmortem | \`l3-support\` |
| Pattern extraction from session, retro to \`lessons.md\` | \`continuous-learner\` |

Rule of thumb: if a file pattern or topic matches above, use that specialist
**before** falling back to \`general-purpose\`. Specialist agents catch
domain-specific bugs (race conditions in migrations, prompt-injection vectors,
TCPA violations) that a generalist won't flag.

When in doubt, run two agents in parallel: the specialist + general-purpose,
and reconcile their outputs.

## Style + conventions

- Tests **before** implementation (RED → GREEN → REFACTOR). Coverage 80%+ default.
- Conventional commits: \`feat\` / \`fix\` / \`docs\` / \`refactor\` / \`test\` / \`chore\`.
- One concern per PR. Refactors and behaviour changes are separate PRs.
- No secrets in code. \`great-cto scan\` blocks ~13 patterns by default.

## Out of scope

Do not invent business decisions. If the spec is ambiguous, ask. Do not skip
gates "because it's just a small change" — that's the path to incidents.

---

_Generated by \`great-cto@${getCliVersion()}\` adapt at ${new Date().toISOString().slice(0, 10)}_
`;
}

