#!/usr/bin/env node
// scripts/agent-changelog.mjs — Phase 3: evolutionary memory per agent.
//
// Ports SIA's context_manager._generate_llm_summary (context.md) to great_cto:
// renders a per-agent generational changelog from the prompt-evolution ledger
// written by scripts/prompt-evolve.mjs. Each generation becomes a row:
//   what changed (lesson) · prompt hash delta · eval delta · verdict.
//
// Unlike SIA this changelog is DETERMINISTIC (no LLM needed) — it reads the
// recorded gate deltas. An optional one-line LLM narrative can be layered later.
//
// Usage:
//   node scripts/agent-changelog.mjs --agent security-officer            # print markdown
//   node scripts/agent-changelog.mjs --agent security-officer --write    # also write file
//
// Output file: .great_cto/agent-reviews/<agent>.changelog.md
//
// Exit: 0 ok | 2 bad args.

import { writeFileSync, mkdirSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import { homedir } from 'node:os';
import { readLedger } from './prompt-evolve.mjs';

const GREAT_CTO_HOME = process.env.GREAT_CTO_HOME || join(homedir(), '.great_cto');
export const REVIEWS_ROOT = join(GREAT_CTO_HOME, 'agent-reviews');

/** Average holdout pass-rate implied by a generation's gate (candidate side). */
function candRate(gen) {
  const impr = gen.gate?.improvements ?? [];
  const regs = gen.gate?.regressions ?? [];
  const rows = [...impr, ...regs];
  if (rows.length === 0) return null;
  const avg = rows.reduce((a, r) => a + (r.candRate ?? 0), 0) / rows.length;
  return avg;
}

/** One-line delta summary between a generation and its parent. */
export function summarizeDelta(gen) {
  const impr = gen.gate?.improvements?.length ?? 0;
  const regs = gen.gate?.regressions?.length ?? 0;
  if (gen.verdict === 'rejected') return `rejected — ${regs} regression(s) on holdout`;
  if (impr > 0 && regs === 0) return `promoted — ${impr} eval(s) improved, no regressions`;
  if (impr === 0 && regs === 0) return 'promoted — held steady on holdout (no delta)';
  return `promoted — ${impr} improved / ${regs} regressed (within tolerance)`;
}

function pct(n) {
  return typeof n === 'number' ? `${(n * 100).toFixed(0)}%` : '—';
}

/** Render the full markdown changelog for an agent. Pure. */
export function renderChangelog(agent, generations) {
  const lines = [];
  lines.push(`# Evolutionary changelog — ${agent}`);
  lines.push('');
  lines.push('> Generated by `scripts/agent-changelog.mjs` from the prompt-evolution ledger.');
  lines.push('> Each row is one self-improvement generation (lesson → candidate → holdout gate).');
  lines.push('');

  if (!generations || generations.length === 0) {
    lines.push('_No evolution generations recorded yet. Run `/prompt-evolve <agent>`._');
    lines.push('');
    return lines.join('\n');
  }

  const promoted = generations.filter(g => g.verdict === 'promoted').length;
  const rejected = generations.filter(g => g.verdict === 'rejected').length;
  lines.push(`**Generations:** ${generations.length}  ·  **Promoted:** ${promoted}  ·  **Rejected:** ${rejected}`);
  lines.push('');
  lines.push('| Gen | Date | Verdict | Holdout | Lesson | Change |');
  lines.push('|---|---|---|---|---|---|');
  for (const g of generations) {
    const date = (g.ts || '—').slice(0, 10);
    const icon = g.verdict === 'promoted' ? '✓ promoted' : '✗ rejected';
    const rate = pct(candRate(g));
    const lesson = (g.lesson || '—').replace(/\|/g, '\\|').slice(0, 80);
    const change = summarizeDelta(g).replace(/\|/g, '\\|');
    lines.push(`| ${g.gen} | ${date} | ${icon} | ${rate} | ${lesson} | ${change} |`);
  }
  lines.push('');

  // Most recent promoted generation detail (the live prompt's provenance)
  const latest = [...generations].reverse().find(g => g.verdict === 'promoted');
  if (latest) {
    lines.push('## Current prompt provenance');
    lines.push('');
    lines.push(`- **Generation:** ${latest.gen}`);
    lines.push(`- **Prompt sha256:** \`${latest.promptHash}\``);
    if (latest.parentHash) lines.push(`- **Parent:** \`${latest.parentHash}\``);
    lines.push(`- **Driven by lesson:** ${latest.lesson || '—'}`);
    lines.push(`- **Gate:** ${latest.gate?.summary || '—'}`);
    lines.push('');
  }
  return lines.join('\n');
}

// ── CLI ──────────────────────────────────────────────────────────────────────

function parseArgs(argv) {
  const out = { write: false };
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a === '--agent') out.agent = argv[++i];
    else if (a === '--write') out.write = true;
    else if (a === '--root') out.root = argv[++i];
    else if (a === '--reviews-root') out.reviewsRoot = argv[++i];
  }
  return out;
}

function main() {
  const args = parseArgs(process.argv.slice(2));
  if (!args.agent) {
    console.error('Usage: node scripts/agent-changelog.mjs --agent <name> [--write]');
    process.exit(2);
  }
  const gens = readLedger(args.agent, args.root);
  const md = renderChangelog(args.agent, gens);
  if (args.write) {
    const root = args.reviewsRoot || REVIEWS_ROOT;
    mkdirSync(root, { recursive: true });
    const p = join(root, `${args.agent}.changelog.md`);
    writeFileSync(p, md);
    console.error(`Wrote ${p}`);
  }
  process.stdout.write(md + '\n');
}

const isMain = process.argv[1] && fileURLToPath(import.meta.url) === process.argv[1];
if (isMain) main();
